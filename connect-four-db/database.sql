    -- ходы крестики-нолики
    PROCEDURE make_ttt_move(
        p_game_id   NUMBER,
        p_player_id NUMBER,
        p_row       NUMBER,
        p_col       NUMBER,
        p_symbol    CHAR
    ) IS
        v_exists NUMBER;
        v_move   NUMBER;
        v_last_symbol CHAR(1);
        v_assigned_symbol CHAR(1);
    BEGIN
        IF p_row NOT BETWEEN 1 AND 3 OR p_col NOT BETWEEN 1 AND 3 THEN
            raise_application_error(-20060, 'Неверные координаты (должны быть 1..3)');
        END IF;
        SELECT symbol INTO v_assigned_symbol
        FROM TicTacToe_Sessions
        WHERE id_game = p_game_id AND id_player = p_player_id;
        IF v_assigned_symbol IS NULL THEN
            raise_application_error(-20061, 'Игроку не назначен символ для крестиков-ноликов');
        END IF;
        IF p_symbol IS NULL OR UPPER(p_symbol) <> v_assigned_symbol THEN
            raise_application_error(-20062, 'Неверный символ для этого игрока');
        END IF;
    
        BEGIN
            SELECT symbol
            INTO v_last_symbol
            FROM (
                SELECT symbol
                FROM TicTacToe_Rounds
                WHERE id_game = p_game_id
                ORDER BY move_number DESC
            ) t
            WHERE ROWNUM = 1;
        EXCEPTION WHEN NO_DATA_FOUND THEN
            v_last_symbol := NULL;
        END;
    
        IF v_last_symbol IS NULL THEN
            IF p_symbol <> 'X' THEN
                raise_application_error(-20051, 'Первым ходит X');
            END IF;
        ELSE
            IF v_last_symbol = 'X' AND p_symbol <> 'O' THEN
                raise_application_error(-20052, 'Сейчас ход O');
            ELSIF v_last_symbol = 'O' AND p_symbol <> 'X' THEN
                raise_application_error(-20053, 'Сейчас ход X');
            END IF;
        END IF;
        SELECT COUNT(*) INTO v_exists
        FROM TicTacToe_Rounds
        WHERE id_game = p_game_id
          AND row_pos = p_row
          AND col_pos = p_col;
        IF v_exists > 0 THEN
            raise_application_error(-20050, 'Клетка уже занята');
        END IF;
        SELECT NVL(MAX(move_number), 0) + 1 INTO v_move FROM TicTacToe_Rounds WHERE id_game = p_game_id;
        INSERT INTO TicTacToe_Rounds(id_game, move_number, id_player, row_pos, col_pos, symbol)
        VALUES (p_game_id, v_move, p_player_id, p_row, p_col, UPPER(p_symbol));
    
        COMMIT;
    EXCEPTION WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
    END make_ttt_move;
   
    -- победитель в крестиках
    FUNCTION ttt_check_winner(p_game_id NUMBER) RETURN NUMBER IS
        TYPE t_matrix IS TABLE OF CHAR(1) INDEX BY VARCHAR2(2);
        board     t_matrix;
        winner_id NUMBER := NULL;
    
        FUNCTION get_cell(pos VARCHAR2) RETURN CHAR IS
        BEGIN
            IF board.EXISTS(pos) THEN
                RETURN board(pos);
            ELSE
                RETURN NULL;
            END IF;
        END;
    BEGIN
        FOR r IN (
            SELECT row_pos, col_pos, symbol
            FROM TicTacToe_Rounds
            WHERE id_game = p_game_id
        ) LOOP
            board(r.row_pos || r.col_pos) := r.symbol;
        END LOOP;
        DECLARE
            TYPE t_line  IS VARRAY(3) OF VARCHAR2(2);
            TYPE t_lines IS VARRAY(8) OF t_line;
            lines t_lines := t_lines(
                t_line('11','12','13'), 
                t_line('21','22','23'),
                t_line('31','32','33'), 
                t_line('11','21','31'),
                t_line('12','22','32'), 
                t_line('13','23','33'), 
                t_line('11','22','33'), 
                t_line('13','22','31')  
            );
        BEGIN
            FOR i IN 1..lines.COUNT LOOP
                DECLARE
                    p1 CHAR(1) := get_cell(lines(i)(1));
                    p2 CHAR(1) := get_cell(lines(i)(2));
                    p3 CHAR(1) := get_cell(lines(i)(3));
                BEGIN
                    IF p1 IS NOT NULL AND p1 = p2 AND p2 = p3 THEN
                        SELECT id_player INTO winner_id
                        FROM TicTacToe_Rounds
                        WHERE id_game = p_game_id
                          AND symbol = p1
                          AND ROWNUM = 1;
    
                        RETURN winner_id;
                    END IF;
                END;
            END LOOP;
        END;
    
        DECLARE
            total_moves NUMBER;
        BEGIN
            SELECT COUNT(*) INTO total_moves
            FROM TicTacToe_Rounds
            WHERE id_game = p_game_id;
            IF total_moves = 9 THEN
                RETURN -1;
            END IF;
        END;
        RETURN NULL; 
    END ttt_check_winner;
   
    -- игра крестики
    PROCEDURE tic_tac_toe_resolve(p_game_id NUMBER) IS
        v_p1 NUMBER;
        v_p2 NUMBER;
        v_symbol_p1 CHAR(1);
        v_symbol_p2 CHAR(1);
        v_turn NUMBER;
        v_winner NUMBER;
        v_round_count NUMBER := 0;
        v_max_rounds NUMBER := 100;
    BEGIN
        SELECT MIN(id_player), MAX(id_player)
        INTO v_p1, v_p2
        FROM Current_game
        WHERE id_game = p_game_id;
    
        IF v_p1 IS NULL OR v_p2 IS NULL THEN
            raise_application_error(-20070, 'Недостаточно игроков для крестиков-ноликов');
        END IF;
    
        <<new_round>>
        v_round_count := v_round_count + 1;
        IF v_round_count > v_max_rounds THEN
            UPDATE Game_history
            SET result = 'Ничья — крестики-нолики (превышено число раундов)',
                end_time = SYSDATE,
                duration = ROUND((SYSDATE - start_time)*24*3600),
                winner_id = NULL
            WHERE id_game = p_game_id;
            COMMIT;
            update_rating(p_game_id);
            RETURN;
        END IF;

        IF DBMS_RANDOM.VALUE(0,1) < 0.5 THEN
            v_symbol_p1 := 'X'; v_symbol_p2 := 'O';
        ELSE
            v_symbol_p1 := 'O'; v_symbol_p2 := 'X';
        END IF;
    
        BEGIN
            MERGE INTO TicTacToe_Sessions ts
            USING (SELECT p_game_id pid, v_p1 p1, v_symbol_p1 s1, v_p2 p2, v_symbol_p2 s2 FROM dual) src
            ON (ts.id_game = src.pid AND ts.id_player = src.p1)
            WHEN MATCHED THEN UPDATE SET ts.symbol = src.s1
            WHEN NOT MATCHED THEN INSERT (id_game, id_player, symbol) VALUES (src.pid, src.p1, src.s1);
    
            MERGE INTO TicTacToe_Sessions ts2
            USING (SELECT p_game_id pid, v_p2 p2, v_symbol_p2 s2 FROM dual) src2
            ON (ts2.id_game = src2.pid AND ts2.id_player = src2.p2)
            WHEN MATCHED THEN UPDATE SET ts2.symbol = src2.s2
            WHEN NOT MATCHED THEN INSERT (id_game, id_player, symbol) VALUES (src2.pid, src2.p2, src2.s2);
        EXCEPTION WHEN OTHERS THEN ROLLBACK; RAISE;
        END;
        COMMIT;
    
        IF v_symbol_p1 = 'X' THEN
            v_turn := v_p1;
        ELSE
            v_turn := v_p2;
        END IF;
    
        DELETE FROM TicTacToe_Rounds WHERE id_game = p_game_id;
        COMMIT;

        LOOP
            NULL;
            v_winner := ttt_check_winner(p_game_id);
    
            IF v_winner IS NOT NULL THEN
                IF v_winner = -1 THEN
                    DELETE FROM TicTacToe_Rounds WHERE id_game = p_game_id;
                    COMMIT;
                    GOTO new_round;
                ELSE
                    DECLARE v_name VARCHAR2(100);
                    BEGIN
                        SELECT name INTO v_name FROM Players WHERE id_player = v_winner;
                        UPDATE Game_history
                        SET result = 'Победа игрока '||v_name||' (крестики-нолики)',
                            winner_id = v_winner,
                            end_time = SYSDATE,
                            duration = ROUND((SYSDATE - start_time)*24*3600)
                        WHERE id_game = p_game_id;
                        COMMIT;
                    END;
                    update_rating(p_game_id);
                    RETURN;
                END IF;
            END IF;
    
            DECLARE total_moves NUMBER;
            BEGIN
                SELECT COUNT(*) INTO total_moves FROM TicTacToe_Rounds WHERE id_game = p_game_id;
                IF total_moves >= 9 THEN
                    DELETE FROM TicTacToe_Rounds WHERE id_game = p_game_id;
                    COMMIT;
                    GOTO new_round;
                END IF;
            END;
        END LOOP;
    EXCEPTION WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
    END tic_tac_toe_resolve;



