CREATE TABLE Players (
    id_player NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    name VARCHAR2(50) NOT NULL UNIQUE,
    reg_date DATE DEFAULT SYSDATE NOT NULL
);

CREATE TABLE Game_history (
    id_game NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    start_time DATE DEFAULT SYSDATE NOT NULL,
    end_time DATE,
    duration NUMBER,
    result VARCHAR2(100) NOT NULL,
    winner_id NUMBER,
    width NUMBER NOT NULL,
    height NUMBER NOT NULL,
    timer_enabled CHAR(1) DEFAULT 'N' CHECK (timer_enabled IN ('Y','N')),
    current_player_id NUMBER,
    CONSTRAINT fk_game_winner FOREIGN KEY (winner_id) REFERENCES Players(id_player) ON DELETE SET NULL,
    CONSTRAINT fk_game_current_player FOREIGN KEY (current_player_id) REFERENCES Players(id_player) ON DELETE SET NULL
);

CREATE TABLE Current_game (
    id_game NUMBER NOT NULL,
    id_player NUMBER NOT NULL,
    token VARCHAR2(10),
    CONSTRAINT pk_current_game PRIMARY KEY (id_game, id_player),
    CONSTRAINT fk_curr_game FOREIGN KEY (id_game) REFERENCES Game_history(id_game) ON DELETE CASCADE,
    CONSTRAINT fk_curr_player FOREIGN KEY (id_player) REFERENCES Players(id_player) ON DELETE CASCADE
);

CREATE TABLE Moves (
    id_move NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    id_game NUMBER NOT NULL,
    id_player NUMBER NOT NULL,
    move_time NUMBER,
    move_number NUMBER NOT NULL,
    row_pos NUMBER, 
    col_pos NUMBER,
    skip_flag CHAR(1) DEFAULT 'N' CHECK (skip_flag IN ('Y','N')),
    CONSTRAINT fk_move_game FOREIGN KEY (id_game) REFERENCES Game_history(id_game) ON DELETE CASCADE,
    CONSTRAINT fk_move_player FOREIGN KEY (id_player) REFERENCES Players(id_player) ON DELETE CASCADE
);

CREATE TABLE Rating (
    id_player NUMBER PRIMARY KEY,
    wins NUMBER DEFAULT 0,
    losses NUMBER DEFAULT 0,
    draws NUMBER DEFAULT 0,
    success NUMBER DEFAULT 0,
    CONSTRAINT fk_rating_player FOREIGN KEY (id_player) REFERENCES Players(id_player) ON DELETE CASCADE
);

CREATE TABLE TicTacToe_Sessions (
    id_session  NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    id_game     NUMBER NOT NULL,
    id_player   NUMBER NOT NULL,
    symbol      CHAR(1) CHECK (symbol IN ('X','O')),
    CONSTRAINT fk_tttsess_game   FOREIGN KEY (id_game)  REFERENCES Game_history(id_game) ON DELETE CASCADE,
    CONSTRAINT fk_tttsess_player FOREIGN KEY (id_player) REFERENCES Players(id_player)  ON DELETE CASCADE,
    CONSTRAINT unq_tttsess UNIQUE (id_game, id_player)
);

CREATE TABLE TicTacToe_Rounds (
    id_round    NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    id_game     NUMBER NOT NULL,
    move_number NUMBER NOT NULL,
    id_player   NUMBER NOT NULL,
    row_pos     NUMBER NOT NULL CHECK (row_pos BETWEEN 1 AND 3),
    col_pos     NUMBER NOT NULL CHECK (col_pos BETWEEN 1 AND 3),
    symbol      CHAR(1) NOT NULL CHECK (symbol IN ('X','O')),
    CONSTRAINT fk_tttr_game   FOREIGN KEY (id_game)   REFERENCES Game_history(id_game) ON DELETE CASCADE,
    CONSTRAINT fk_tttr_player FOREIGN KEY (id_player) REFERENCES Players(id_player)   ON DELETE CASCADE
);




